#include<bits/stdc++.h>
using namespace std;

int main(){


    // Demonsration of for loop 
    cout<<"Printing 1 to 5 integers using for loop"<<endl;
    for(int i = 0; i < 5; i++){
        cout << i << " ";
    }
    cout << endl;

    int i=0;
 // Demonsration of while loop
    while(i < 5){
        cout << i << " ";
        i++;
    }
    cout<<endl;

    // Demonstration of do while loop

    int k = 0;
    do
    {
        cout<< k << " "; 
        k++;
    } while (k < 5);
    




}